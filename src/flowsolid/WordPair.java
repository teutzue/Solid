
package flowsolid;

/**
 *
 * @author CosticaTeodor
 */
public class WordPair {
    String question;
    String answer;
    
    public WordPair(String question, String answer){
    
    this.question = question;
    this.answer = answer;
    }

    public String getQuestion() {
        return question;
    }

    public String getAnswer() {
        return answer;
    }
}
